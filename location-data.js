// 初始化地区选择器
function initializeLocationSelectors() {
    const province = document.getElementById('province');
    const city = document.getElementById('city');
    const county = document.getElementById('county');
    
    // 清空现有选项
    province.innerHTML = '<option value="">选择省份</option>';
    city.innerHTML = '<option value="">选择城市</option>';
    county.innerHTML = '<option value="">选择区县</option>';
    
    // 添加省份选项
    Object.keys(locationData).forEach(provinceName => {
        const option = document.createElement('option');
        option.value = provinceName;
        option.textContent = provinceName;
        province.appendChild(option);
    });
}

// 处理地区选择变化
function handleLocationChange() {
    const province = document.getElementById('province');
    const city = document.getElementById('city');
    const county = document.getElementById('county');
    
    if (province.value) {
        // 更新城市选项
        city.innerHTML = '<option value="">选择城市</option>';
        county.innerHTML = '<option value="">选择区县</option>';
        
        Object.keys(locationData[province.value]).forEach(cityName => {
            const option = document.createElement('option');
            option.value = cityName;
            option.textContent = cityName;
            city.appendChild(option);
        });
    }
    
    if (city.value) {
        // 更新区县选项
        county.innerHTML = '<option value="">选择区县</option>';
        
        locationData[province.value][city.value].forEach(countyName => {
            const option = document.createElement('option');
            option.value = countyName;
            option.textContent = countyName;
            county.appendChild(option);
        });
    }
} 